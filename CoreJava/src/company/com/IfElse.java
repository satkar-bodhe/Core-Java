package company.com;


// If else conditional example 

public class IfElse {

	
	//if age is more than 60 then, eligible for 15% discount.
	
	//if age is less than 60 then, not eligible for discount. 
		
	
	public void ticketBookingDiscount(int age) {
	
	if (age > 60) 
	{
		System.out.println("You are eligible for the 15% discount");
	}
	else 
	{
		System.out.println("You are not eligible for the 15% discount");
	
	}	
		
	}
	
}
